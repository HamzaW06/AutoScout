/**
 * Seed the model_intelligence table with 80+ entries covering the most common
 * reliable used cars. Skips seeding if data already exists.
 */
export async function seedModelIntelligence(): Promise<void> {
  const db = getDb();

  const row = db.get<{ count: number }>('SELECT COUNT(*) as count FROM model_intelligence');
  if (row && row.count > 0) {
    console.log('Model intelligence already seeded');
    return;
  }

  db.transaction(() => {
    for (const entry of ALL_ENTRIES) {
      db.run(INSERT_SQL, toParams(entry));
    }
  });

  console.log(`Seeded ${ALL_ENTRIES.length} model intelligence entries`);
}
