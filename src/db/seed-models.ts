interface ModelEntry {
  make: string;
  model: string;
  year_min: number;
  year_max: number;
  engine: string;
  transmission: string;
  timing_type: 'chain' | 'belt';
  timing_interval_miles: number | null;
  known_issues: string[];
  critical_checks: string[];
  avoid_if: string[];
  reliability_score: number;
  avg_annual_repair_cost: number;
  expected_lifespan_miles: number;
  oil_type: string;
  oil_change_interval_miles: number;
  repair_schedule: object[];
  failure_points: object[];
  notes: string;
}

